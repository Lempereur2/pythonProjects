# Ввод строки
text = input("Введите строку: ")

# Подсчет количества букв "а" (с учетом регистра)
count = text.lower().count("а")

# Удаление всех букв "а" (с учетом регистра)
new_text = text.replace("а", "").replace("А", "")

# Вывод результатов
print(f"Изменённая строка: {new_text}")
print(f"Количество удалённых символов 'а': {count}")