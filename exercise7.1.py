# Функция для нахождения НОД с использованием алгоритма Евклида
def gcd(a, b):
    while b != 0:
        a, b = b, a % b
    return a

# Функция для нахождения НОК
def lcm(a, b):
    return (a * b) // gcd(a, b)

# Основная программа
def main():
    print("Программа для нахождения НОД и НОК двух чисел.")
    a = int(input("Введите первое натуральное число A: "))
    b = int(input("Введите второе натуральное число B: "))

    if a <= 0 or b <= 0:
        print("Ошибка: оба числа должны быть натуральными (положительными).")
        return

    # Нахождение НОД и НОК
    nod = gcd(a, b)
    nok = lcm(a, b)

    # Вывод результата
    print(f"Наибольший общий делитель (НОД) чисел {a} и {b}: {nod}")
    print(f"Наименьшее общее кратное (НОК) чисел {a} и {b}: {nok}")

# Запуск программы
main()