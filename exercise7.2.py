import math

def calculate_area(a, b, c, d, diagonal):
    """
    Вычисляет площадь выпуклого четырехугольника с заданными сторонами и диагональю.
    """
    # Вычисление полупериметра для первого треугольника (ABC)
    p1 = (a + b + diagonal) / 2

    # Площадь первого треугольника (ABC) по формуле Герона
    area1 = math.sqrt(p1 * (p1 - a) * (p1 - b) * (p1 - diagonal))

    # Вычисление полупериметра для второго треугольника (CDA)
    p2 = (c + d + diagonal) / 2

    # Площадь второго треугольника (CDA) по формуле Герона
    area2 = math.sqrt(p2 * (p2 - c) * (p2 - d) * (p2 - diagonal))

    # Суммарная площадь четырехугольника
    total_area = area1 + area2

    return total_area

# Основная программа
def main():
    print("Программа вычисления площади выпуклого четырехугольника.")
    a = float(input("Введите длину первой стороны (a): "))
    b = float(input("Введите длину второй стороны (b): "))
    c = float(input("Введите длину третьей стороны (c): "))
    d = float(input("Введите длину четвертой стороны (d): "))
    diagonal = float(input("Введите длину диагонали: "))

    # Проверка корректности значений
    if a <= 0 or b <= 0 or c <= 0 or d <= 0 or diagonal <= 0:
        print("Ошибка: длины сторон и диагонали должны быть положительными числами.")
        return

    # Вычисление площади
    area = calculate_area(a, b, c, d, diagonal)
    print(f"Площадь выпуклого четырехугольника: {area:.2f}")

# Запуск программы
main()